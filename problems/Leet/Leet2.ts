export function leetTranslator2(message: string) {
  let translatedMessage = message
    .toLowerCase()
    .split("")
    .map((char) => {
      const charIndex = letters2.indexOf(char);
      return leetChars2[charIndex];
    });
  return translatedMessage.join("");
}

